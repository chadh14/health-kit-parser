class Record():

    def __init__(self, type, value, unit, source_name, creation_date, start_date, end_date):
        self.type = type
        self.value = value
        self.unit = unit
        self.source_name = source_name
        self.creation_date = creation_date
        self.start_date = start_date
        self.end_date = end_date
